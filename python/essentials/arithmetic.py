#addition
a = 5
b = 3
c = 5 + 3
print(f'addition of {a} and {b} yields {c}')

#subtraction
c = 5 - 3
print(f'subtraction of {a} and {b} yields {c}')

#multiplication
c = 5 * 3
print(f'multiplication of {a} and {b} yields {c}')

#division
c = 5 / 3
print(f'division of {a} and {b} yields {c}')

#integer division
c = 5 // 3
print(f'integer division of {a} and {b} yields {c}')

#remainder (modulo)
c = 5 % 3
print(f'remainder of {a} and {b} yields {c}')

#exponent
c = 5 ** 3
print(f'exponent of {a} and {b} yields {c}')

#unary negative
c = 5 ** 3
print(f'exponent of {a} and {b} yields {c}')

#unary positive
c = 5 ** 3
print(f'exponent of {a} and {b} yields {c}')


